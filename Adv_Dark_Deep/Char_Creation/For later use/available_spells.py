# Function to retrieve spells for a given class and level
def get_spells_by_class_and_level(class_name, level):
    return spells_data.get(class_name, {}).get(level, [])

# Example usage
class_name = "Illusionist"
level = "3rd Level"
spells = get_spells_by_class_and_level(class_name, level)
print(f"{class_name} {level} Spells: {spells}")

# Function to add a new spell to a class and level
def add_spell_to_class_level(class_name, level, spell_name):
    if class_name in spells_data:
        if level in spells_data[class_name]:
            spells_data[class_name][level].append(spell_name)
        else:
            spells_data[class_name][level] = [spell_name]
    else:
        spells_data[class_name] = {level: [spell_name]}

# Example of adding a new spell
add_spell_to_class_level("Illusionist", "3rd Level", "New Illusion Spell")
print(f"{class_name} {level} Spells: {get_spells_by_class_and_level(class_name, level)}")

